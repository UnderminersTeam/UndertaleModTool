    public class VariableInfo
    {
        public int ID; // Nothing special, just increments?
        public string SetterFunction;
        public string GetterFunction;
        public bool CanGet;
        public bool CanSet;

        public VariableInfo()
        {

        }

        public VariableInfo(BuiltinList list, bool canGet, bool canSet)
        {
            CanGet = canGet;
            CanSet = canSet;

            ID = list.CurrentID++;
        }

        public VariableInfo(BuiltinList list, bool canGet, bool canSet, string setterFunction)
        {
            CanGet = canGet;
            CanSet = canSet;
            SetterFunction = setterFunction;

            ID = list.CurrentID++;
        }

        public VariableInfo(BuiltinList list, bool canGet, bool canSet, string setterFunction, string getterFunction)
        {
            CanGet = canGet;
            CanSet = canSet;
            SetterFunction = setterFunction;
            GetterFunction = getterFunction;

            ID = list.CurrentID++;
        }
    }

    public class FunctionInfo
    {
        public int ArgumentCount;
        public bool IsFirstParamInstance = false;
        public bool IsFirstParamOther = false;
        public int ID;
        public FunctionClassification Classification = FunctionClassification.None;

        public FunctionInfo(BuiltinList list, int argumentCount)
        {
            ArgumentCount = argumentCount;
            ID = list.CurrentID++;
        }

        public FunctionInfo(BuiltinList list, int argumentCount, FunctionClassification classification) : this(list, argumentCount)
        {
            Classification = classification;
            // Maybe handle this differently? (look UndertaleFunction.cs)
        }

        public FunctionInfo(BuiltinList list, int argumentCount, bool isFirstParamInstance)
        {
            ArgumentCount = argumentCount;
            IsFirstParamInstance = isFirstParamInstance;
            ID = list.CurrentID++;
        }

        public FunctionInfo(BuiltinList list, int argumentCount, bool isFirstParamInstance, bool isFirstParamOther)
        {
            ArgumentCount = argumentCount;
            IsFirstParamInstance = isFirstParamInstance;
            IsFirstParamOther = isFirstParamOther;
            ID = list.CurrentID++;
        }
    }

